// Bundle packages/mcp/prompts/*.md into one generated constant file,
// packages/mcp/src/guides/AutoMovieGuideConstant.ts, gitignored and rebuilt on
// every install/build. The unified AutoMovieApplication exposes each document
// by exact SCREAMING_SNAKE_CASE filename stem through getGuideDocument({ name }).
// One file, one export.
import { mkdirSync, readFileSync, readdirSync, writeFileSync } from "node:fs";
import { dirname, resolve } from "node:path";
import { fileURLToPath } from "node:url";

const HERE = dirname(fileURLToPath(import.meta.url));
const PACKAGE_ROOT = resolve(HERE, "..");
const PROMPTS_ROOT = resolve(PACKAGE_ROOT, "prompts");
const OUT_DIR = resolve(PACKAGE_ROOT, "src/guides");
const OUT_FILE = resolve(OUT_DIR, "AutoMovieGuideConstant.ts");
mkdirSync(OUT_DIR, { recursive: true });

const files = readdirSync(PROMPTS_ROOT).filter((f) => f.endsWith(".md"));

const record = {};
for (const file of files) {
  const stem = file.slice(0, -".md".length);
  if (!/^[A-Z][A-Z0-9_]*$/.test(stem))
    throw new Error(
      `Prompt file "${file}" is not a SCREAMING_SNAKE_CASE guide key (expected e.g. AUTOMOVIE_OVERALL.md or STAGING.md)`,
    );
  record[stem] = readFileSync(resolve(PROMPTS_ROOT, file), "utf8")
    .replaceAll("\r\n", "\n")
    .trim();
}

const sorted = Object.fromEntries(
  Object.keys(record)
    .sort()
    .map((stem) => [stem, record[stem]]),
);

const out =
  `// GENERATED by build/prompt.mjs from packages/mcp/prompts/*.md\n` +
  `// Do not edit by hand; edit the source markdown and re-run \`pnpm build:prompt\`.\n` +
  `// Gitignored: this file is rebuilt on every install ("prepare") and build.\n\n` +
  `/** Guide content for AutoMovieApplication.getGuideDocument, keyed by exact prompts/*.md filename stem (e.g. "AUTOMOVIE_OVERALL"). */\n` +
  `export const AUTOMOVIE_GUIDE_CONSTANT = {\n` +
  Object.entries(sorted)
    .map(([stem, content]) => `  ${stem}:\n    ${JSON.stringify(content)},`)
    .join("\n") +
  `\n} as const;\n`;

writeFileSync(OUT_FILE, out, "utf8");
console.log(`wrote ${files.length} topic(s) -> ${OUT_FILE}`);
